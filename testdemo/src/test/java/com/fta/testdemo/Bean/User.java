package com.fta.testdemo.Bean;

public class User {

    private String name;
    private int age;
    private Integer integerAge;
    private long longAge;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public Integer getIntegerAge() {
        return integerAge;
    }

    public void setIntegerAge(Integer integerAge) {
        this.integerAge = integerAge;
    }

    public long getLongAge() {
        return longAge;
    }

    public void setLongAge(long longAge) {
        this.longAge = longAge;
    }
}
